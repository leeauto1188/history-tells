def js_str(s):
    return s.replace('\\', '\\\\').replace('"', '\\"').replace('\n', '\\n')

def serialize(e):
    figs = ",".join(
        f'{{name:"{js_str(f["name"])}",role:"{js_str(f["role"])}"}}'
        for f in e["figures"]
    )
    reads = ",".join(f'"{js_str(r)}"' for r in e["reading"])
    return (
        f'  {{ month:{e["month"]},day:{e["day"]},year:"{js_str(e["year"])}",'
        f'title:"{js_str(e["title"])}",'
        f'description:"{js_str(e["description"])}",'
        f'figures:[{figs}],'
        f'impact:"{js_str(e["impact"])}",'
        f'insight:"{js_str(e["insight"])}",'
        f'reading:[{reads}]}}'
    )

def inject(html, line):
    m = re.search(r'(\n\];\s*//\s*END_EVENTS\n)', html) or \
        re.search(r'(\n\];\n\nconst QUOTES)', html)
    if not m:
        raise RuntimeError("找不到注入点")
    p = m.start() + 1
    return html[:p] + line + ",\n" + html[p:]

